package gestion_des_exams;

public class Etudiant {
	private String nom;
	private String cin;
	private String [] liste_desmodule;
	public String getNom() {
		return nom;
	}
	public void setNom(String nom) {
		this.nom = nom;
	}
	public String getCin() {
		return cin;
	}
	public void setCin(String cin) {
		this.cin = cin;
	}
	public String[] getListe_desmodule() {
		return liste_desmodule;
	}
	public void setListe_desmodule(String[] liste_desmodule) {
		this.liste_desmodule = liste_desmodule;
	}
	
	

}
